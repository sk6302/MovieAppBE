package com.mvd.movielist;

public class MovieLists {
	
	int movieid;
	String moviename;
	String movieLanguage;
	String moviebudget;
	String moviedirector;
	String movietrending;
	String moviecategory;
	String releaseyear;
	String movierating;
	
	public int getMovieid() {
		return movieid;
	}
	public void setMovieid(int movieid) {
		this.movieid = movieid;
	}
	public String getMoviename() {
		return moviename;
	}
	public void setMoviename(String moviename) {
		this.moviename = moviename;
	}
	public String getMovieLanguage() {
		return movieLanguage;
	}
	public void setMovieLanguage(String movieLanguage) {
		this.movieLanguage = movieLanguage;
	}
	public String getMoviebudget() {
		return moviebudget;
	}
	public void setMoviebudget(String moviebudget) {
		this.moviebudget = moviebudget;
	}
	public String getMoviedirector() {
		return moviedirector;
	}
	public void setMoviedirector(String moviedirector) {
		this.moviedirector = moviedirector;
	}
	public String getMovietrending() {
		return movietrending;
	}
	public void setMovietrending(String movietrending) {
		this.movietrending = movietrending;
	}
	public String getMoviecategory() {
		return moviecategory;
	}
	public void setMoviecategory(String moviecategory) {
		this.moviecategory = moviecategory;
	}
	public String getReleaseyear() {
		return releaseyear;
	}
	public void setReleaseyear(String releaseyear) {
		this.releaseyear = releaseyear;
	}
	public String getMovierating() {
		return movierating;
	}
	public void setMovierating(String movierating) {
		this.movierating = movierating;
	}
	
	

}
